
class cartProductItem{
   String name ='';
   String price ='';
   String quantity ='';
   String modifier='';
   String variant='';

   cartProductItem(String name, String price, String quantity, String modifier, String variant){
       this.name = name;
       this.price = price;
       this.quantity = quantity;
       this.modifier = modifier;
       this.variant = variant;
   }

}